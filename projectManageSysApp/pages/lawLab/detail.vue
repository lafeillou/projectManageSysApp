			<text class="title">最高人民法院 最高人民检察院 公安部 司法部\n《关于办理恶势力刑事案件若干问题的意见》</text>
			<text class="text-body">
				为认真贯彻落实中央开展扫黑除恶专项斗争的部署要求，正确理解和适用最高人民法院、最高人民检察院、公安部、司法部《关于办理黑恶势力犯罪案件若干问题的指导意见》（法发﹝2018﹞1号，以下简称《指导意见》），根据刑法、刑事诉讼法及有关司法解释、规范性文件的规定，现对办理恶势力刑事案件若干问题提出如下意见：
				\n\n一、办理恶势力刑事案件的总体要求  
				\n 1．人民法院、人民检察院、公安机关和司法行政机关要深刻认识恶势力违法犯罪的严重社会危害，毫不动摇地坚持依法严惩方针，在侦查、起诉、审判、执行各阶段，运用多种法律手段全面体现依法从严惩处精神，有力震慑恶势力违法犯罪分子，有效打击和预防恶势力违法犯罪。
				\n 2．人民法院、人民检察院、公安机关和司法行政机关要严格坚持依法办案，确保在案件事实清楚，证据确实、充分的基础上，准确认定恶势力和恶势力犯罪集团，坚决防止人为拔高或者降低认定标准。要坚持贯彻落实宽严相济刑事政策，根据犯罪嫌疑人、被告人的主观恶性、人身危险性、在恶势力、恶势力犯罪集团中的地位、作用以及在具体犯罪中的罪责，切实做到宽严有据，罚当其罪，实现政治效果、法律效果和社会效果的统一。 
				\n 3．人民法院、人民检察院、公安机关和司法行政机关要充分发挥各自职能，分工负责，互相配合，互相制约，坚持以审判为中心的刑事诉讼制度改革要求，严格执行“三项规程”，不断强化程序意识和证据意识，有效加强法律监督，确保严格执法、公正司法，充分保障当事人、诉讼参与人的各项诉讼权利。 
				\n\n二、恶势力、恶势力犯罪集团的认定标准  
				\n 4．恶势力，是指经常纠集在一起，以暴力、威胁或者其他手段，在一定区域或者行业内多次实施违法犯罪活动，为非作恶，欺压百姓，扰乱经济、社会生活秩序，造成较为恶劣的社会影响，但尚未形成黑社会性质组织的违法犯罪组织。 
				\n 5．单纯为牟取不法经济利益而实施的“黄、赌、毒、盗、抢、骗”等违法犯罪活动，不具有为非作恶、欺压百姓特征的，或者因本人及近亲属的婚恋纠纷、家庭纠纷、邻里纠纷、劳动纠纷、合法债务纠纷而引发以及其他确属事出有因的违法犯罪活动，不应作为恶势力案件处理。
				\n 6．恶势力一般为3人以上，纠集者相对固定。纠集者，是指在恶势力实施的违法犯罪活动中起组织、策划、指挥作用的违法犯罪分子。成员较为固定且符合恶势力其他认定条件，但多次实施违法犯罪活动是由不同的成员组织、策划、指挥，也可以认定为恶势力，有前述行为的成员均可以认定为纠集者。 
				恶势力的其他成员，是指知道或应当知道与他人经常纠集在一起是为了共同实施违法犯罪，仍按照纠集者的组织、策划、指挥参与违法犯罪活动的违法犯罪分子，包括已有充分证据证明但尚未归案的人员，以及因法定情形不予追究法律责任，或者因参与实施恶势力违法犯罪活动已受到行政或刑事处罚的人员。仅因临时雇佣或被雇佣、利用或被利用以及受蒙蔽参与少量恶势力违法犯罪活动的，一般不应认定为恶势力成员。 
				\n 7．“经常纠集在一起，以暴力、威胁或者其他手段，在一定区域或者行业内多次实施违法犯罪活动”，是指犯罪嫌疑人、被告人于2年之内，以暴力、威胁或者其他手段，在一定区域或者行业内多次实施违法犯罪活动，且包括纠集者在内，至少应有2名相同的成员多次参与实施违法犯罪活动。对于“纠集在一起”时间明显较短，实施违法犯罪活动刚刚达到“多次”标准，且尚不足以造成较为恶劣影响的，一般不应认定为恶势力。 
				\n 8．恶势力实施的违法犯罪活动，主要为强迫交易、故意伤害、非法拘禁、敲诈勒索、故意毁坏财物、聚众斗殴、寻衅滋事，但也包括具有为非作恶、欺压百姓特征，主要以暴力、威胁为手段的其他违法犯罪活动。 
				恶势力还可能伴随实施开设赌场、组织卖淫、强迫卖淫、贩卖毒品、运输毒品、制造毒品、抢劫、抢夺、聚众扰乱社会秩序、聚众扰乱公共场所秩序、交通秩序以及聚众“打砸抢”等违法犯罪活动，但仅有前述伴随实施的违法犯罪活动，且不能认定具有为非作恶、欺压百姓特征的，一般不应认定为恶势力。 
				\n 9．办理恶势力刑事案件，“多次实施违法犯罪活动”至少应包括1次犯罪活动。对于反复实施强迫交易、非法拘禁、敲诈勒索、寻衅滋事等单一性质的违法行为，单次情节、数额尚不构成犯罪，但按照刑法或者有关司法解释、规范性文件的规定累加后应作为犯罪处理的，在认定是否属于“多次实施违法犯罪活动”时，可将已用于累加的违法行为计为1次犯罪活动，其他违法行为单独计算违法活动的次数。 
				已被处理或者已作为民间纠纷调处，后经查证确属恶势力违法犯罪活动的，均可以作为认定恶势力的事实依据，但不符合法定情形的，不得重新追究法律责任。 
				\n 10．认定“扰乱经济、社会生活秩序，造成较为恶劣的社会影响”，应当结合侵害对象及其数量、违法犯罪次数、手段、规模、人身损害后果、经济损失数额、违法所得数额、引起社会秩序混乱的程度以及对人民群众安全感的影响程度等因素综合把握。 
				\n 11．恶势力犯罪集团，是指符合恶势力全部认定条件，同时又符合犯罪集团法定条件的犯罪组织。 
				恶势力犯罪集团的首要分子，是指在恶势力犯罪集团中起组织、策划、指挥作用的犯罪分子。恶势力犯罪集团的其他成员，是指知道或者应当知道是为共同实施犯罪而组成的较为固定的犯罪组织，仍接受首要分子领导、管理、指挥，并参与该组织犯罪活动的犯罪分子。
				恶势力犯罪集团应当有组织地实施多次犯罪活动，同时还可能伴随实施违法活动。恶势力犯罪集团所实施的违法犯罪活动，参照《指导意见》第十条第二款的规定认定。
				\n 12．全部成员或者首要分子、纠集者以及其他重要成员均为未成年人、老年人、残疾人的，认定恶势力、恶势力犯罪集团时应当特别慎重。 
				\n\n 三、正确运用宽严相济刑事政策的有关要求 
				\n 13．对于恶势力的纠集者、恶势力犯罪集团的首要分子、重要成员以及恶势力、恶势力犯罪集团共同犯罪中罪责严重的主犯，要正确运用法律规定加大惩处力度，对依法应当判处重刑或死刑的，坚决判处重刑或死刑。同时要严格掌握取保候审，严格掌握不起诉，严格掌握缓刑、减刑、假释，严格掌握保外就医适用条件，充分利用资格刑、财产刑等法律手段全方位从严惩处。对于符合刑法第三十七条之一规定的，可以依法禁止其从事相关职业。 
				对于恶势力、恶势力犯罪集团的其他成员，在共同犯罪中罪责相对较小、人身危险性、主观恶性相对不大的，具有自首、立功、坦白、初犯等法定或酌定从宽处罚情节，可以依法从轻、减轻或免除处罚。认罪认罚或者仅参与实施少量的犯罪活动且只起次要、辅助作用，符合缓刑条件的，可以适用缓刑。 
				\n 14．恶势力犯罪集团的首要分子检举揭发与该犯罪集团及其违法犯罪活动有关联的其他犯罪线索，如果在认定立功的问题上存在事实、证据或法律适用方面的争议，应当严格把握。依法应认定为立功或者重大立功的，在决定是否从宽处罚、如何从宽处罚时，应当根据罪责刑相一致原则从严掌握。可能导致全案量刑明显失衡的，不予从宽处罚。 
				恶势力犯罪集团的其他成员如果能够配合司法机关查办案件，有提供线索、帮助收集证据或者其他协助行为，并在侦破恶势力犯罪集团案件、查处“保护伞”等方面起到较大作用的，即使依法不能认定立功，一般也应酌情对其从轻处罚。 
				\n 15．犯罪嫌疑人、被告人同时具有法定、酌定从严和法定、酌定从宽处罚情节的，量刑时要根据所犯具体罪行的严重程度，结合被告人在恶势力、恶势力犯罪集团中的地位、作用、主观恶性、人身危险性等因素整体把握。对于恶势力的纠集者、恶势力犯罪集团的首要分子、重要成员，量刑时要体现总体从严。对于在共同犯罪中罪责相对较小、人身危险性、主观恶性相对不大，且能够真诚认罪悔罪的其他成员，量刑时要体现总体从宽。 
				\n 16．恶势力刑事案件的犯罪嫌疑人、被告人自愿如实供述自己的罪行，承认指控的犯罪事实，愿意接受处罚的，可以依法从宽处理，并适用认罪认罚从宽制度。对于犯罪性质恶劣、犯罪手段残忍、社会危害严重的犯罪嫌疑人、被告人，虽然认罪认罚，但不足以从轻处罚的，不适用该制度。 
				\n\n四、办理恶势力刑事案件的其他问题
				\n 17．人民法院、人民检察院、公安机关经审查认为案件符合恶势力认定标准的，应当在起诉意见书、起诉书、判决书、裁定书等法律文书中的案件事实部分明确表述，列明恶势力的纠集者、其他成员、违法犯罪事实以及据以认定的证据；符合恶势力犯罪集团认定标准的，应当在上述法律文书中明确定性，列明首要分子、其他成员、违法犯罪事实以及据以认定的证据，并引用刑法总则关于犯罪集团的相关规定。被告人及其辩护人对恶势力定性提出辩解和辩护意见，人民法院可以在裁判文书中予以评析回应。 
				恶势力刑事案件的起诉意见书、起诉书、判决书、裁定书等法律文书，可以在案件事实部分先概述恶势力、恶势力犯罪集团的概括事实，再分述具体的恶势力违法犯罪事实。
				\n 18．对于公安机关未在起诉意见书中明确认定，人民检察院在审查起诉期间发现构成恶势力或者恶势力犯罪集团，且相关违法犯罪事实已经查清，证据确实、充分，依法应追究刑事责任的，应当作出起诉决定，根据查明的事实向人民法院提起公诉，并在起诉书中明确认定为恶势力或者恶势力犯罪集团。人民检察院认为恶势力相关违法犯罪事实不清、证据不足，或者存在遗漏恶势力违法犯罪事实、遗漏同案犯罪嫌疑人等情形需要补充侦查的，应当提出具体的书面意见，连同案卷材料一并退回公安机关补充侦查；人民检察院也可以自行侦查，必要时可以要求公安机关提供协助。
				对于人民检察院未在起诉书中明确认定，人民法院在审判期间发现构成恶势力或恶势力犯罪集团的，可以建议人民检察院补充或者变更起诉；人民检察院不同意或者在七日内未回复意见的，人民法院不应主动认定，可仅就起诉指控的犯罪事实依照相关规定作出判决、裁定。 
				审理被告人或者被告人的法定代理人、辩护人、近亲属上诉的案件时，一审判决认定黑社会性质组织有误的，二审法院应当纠正,符合恶势力、恶势力犯罪集团认定标准，应当作出相应认定；一审判决认定恶势力或恶势力犯罪集团有误的，应当纠正，但不得升格认定；一审判决未认定恶势力或恶势力犯罪集团的，不得增加认定。 
				\n 19．公安机关、人民检察院、人民法院应当分别以起诉意见书、起诉书、裁判文书所明确的恶势力、恶势力犯罪集团，作为相关数据的统计依据。 
				\n 20．本意见自2019年4月9日起施行。
